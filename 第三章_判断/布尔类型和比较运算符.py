bool_1 = True
bool_2 = False
print(bool_1, type(bool_1))
print(f"{bool_2}, {type(bool_2)}")

num1 = 10
num2 = 10
print(f"10==10的结果是: {num1 == num2}")
print("10=10的结果是: %d" % (num1 == num2))
print(f"10!=10的结果是: {num1 != num2}")

# 输出
'''
True <class 'bool'>
False, <class 'bool'>
10==10的结果是: True
10=10的结果是: 1
10!=10的结果是: False
'''
